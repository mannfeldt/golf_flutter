class Club {
  String name;
  int id;
  String type;
  int loft;
  int powerFactor;
  int max;
  Club(String name, int id, String type, int loft, int powerFactor, int max) {
    this.name = name;
    this.id = id;
    this.type = type;
    this.loft = loft;
    this.powerFactor = powerFactor;
    this.max = max;
  }
}
